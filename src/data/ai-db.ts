export const evaluateMath = (expr: string): string | null => {
  try {
    // Extract math expression from string
    const match = expr.match(/([0-9+\-*/().\s^sqrt|sin|cos|tan]+)/);
    if (!match) return null;
    
    const potentialExpr = match[0].trim();
    if (potentialExpr.length < 3) return null;

    // Basic sanitization
    const sanitized = potentialExpr.replace(/[^0-9+\-*/().\s]/g, '');
    if (!sanitized) return null;
    
    // safe eval alternative
    const result = new Function("return " + sanitized)();
    
    if (typeof result === 'number' && isFinite(result)) {
      return result + " ✅";
    }
    return null;
  } catch (e) {
    return null;
  }
};
